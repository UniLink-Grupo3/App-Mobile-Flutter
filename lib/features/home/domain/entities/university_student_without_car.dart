class UniversityStudentWithoutCar {
  final int id;
  final String name;
  final String code;
  final String university;
  final String image;
  final int numberPeople;
  final String destination;
  final String pickup;
  final int price;

  UniversityStudentWithoutCar({
    required this.id,
    required this.name,
    required this.code,
    required this.university,
    required this.image,
    required this.numberPeople,
    required this.destination,
    required this.pickup,
    required this.price,
  });
}
